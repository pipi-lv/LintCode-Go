func maxmiumScore(cards []int, cnt int) int {
	// 1.å…ˆæ’åº
	sort.Ints(cards)

	// 2.å…ˆå°†åcntä¸ªåŠ èµ·æ¥
	var result int
	var ming, mino = 1000, 1000
	for i := 1; i <= cnt; i++ {
		d := cards[len(cards)-1-(cnt-i)]
		if d%2 == 0 {
			mino = min(mino, d)
		} else {
			ming = min(ming, d)
		}

		result += d
	}

	if result%2 == 0 {
		return result
	}

	// 3.å¦‚æœä¸æ»¡è¶³ï¼Œä»å‰æ‰¾ä¸ªæœ€å¤§çš„å¥‡æ•°å’Œè‡ªå·±é‡Œé¢æœ€å°çš„å¶æ•°äº¤æ¢ï¼›æˆ–è€…æ‰¾ä¸ªæœ€å¤§çš„å¶æ•°å’Œè‡ªå·±æœ€å°çš„å¥‡æ•°äº¤æ¢ï¼›å–ä¸¤ä¸ªä¸­æœ€å¤§çš„
	var maxg, maxo int
	for i := 0; i < len(cards)-cnt; i++ {
		if cards[i]%2 == 0 {
			maxo = max(maxo, cards[i])
		} else {
			maxg = max(maxg, cards[i])
		}
	}

	var result2 int
	if ming != 1000 && maxo > 0 {
		result2 = result - ming + maxo
	}

	if mino != 1000 && maxg > 0 {
		result2 = max(result2, result-mino+maxg)
	}

	return result2
}

